const mongoose = require('mongoose');

// Example model - replace with your actual models
const ExampleSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('Example', ExampleSchema);

